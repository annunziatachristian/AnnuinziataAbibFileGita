    private void btnSvuotaRecordSelezionatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSvuotaRecordSelezionatoActionPerformed
        String nomeGita = jComboBox2.getSelectedItem() != null
            ? jComboBox2.getSelectedItem().toString().trim()
            : "";

    if (nomeGita.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selezionare una gita da eliminare.");
        return;
    }

    int scelta = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "Vuoi davvero eliminare la gita \"" + nomeGita + "\"?",
            "Conferma",
            javax.swing.JOptionPane.YES_NO_OPTION
    );

    if (scelta != javax.swing.JOptionPane.YES_OPTION) {
        return;
    }

    try {
        boolean eliminata = gestione.eliminaGita(nomeGita);

        if (eliminata) {
            caricaComboGite();
            txtArea.setText("La gita \"" + nomeGita + "\" è stata eliminata.");
            if (ultimoRecordInserito.contains(nomeGita)) {
                ultimoRecordInserito = "";
            }
        } else {
            txtArea.setText("Nessuna gita trovata.");
        }

    } catch (java.io.IOException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Errore: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnSvuotaRecordSelezionatoActionPerformed

