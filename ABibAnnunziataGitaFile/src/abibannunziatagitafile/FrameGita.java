     private void inserisciRecord() {
    try {
        String nome = txtNome.getText().trim();
        String cognome = txtCognome.getText().trim();
        String classe = txtClasse.getText().trim();
        String nomeGita = txtNomeGita.getText().trim();
        String dest = txtDestinazione.getText().trim();
        String costoTesto = txtCosto.getText().trim();

        if (nome.isEmpty() || cognome.isEmpty() || classe.isEmpty()
                || nomeGita.isEmpty() || dest.isEmpty() || costoTesto.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Compilare tutti i campi.");
            return;
        }

        double costo = Double.parseDouble(costoTesto);
        boolean iscritto = chkIscritto.isSelected();

        Studente s = new Studente(nome, cognome, classe, iscritto);
        Gita g = new Gita(nomeGita, dest, costo);

        if (gestione.studenteGiaInseritoNellaGita(s, nomeGita)) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "L'alunno è già stato inserito nella gita \"" + nomeGita + "\".");
            return;
        }

        gestione.salvaRecord(s, g);

        ultimoRecordInserito = "Record inserito:\n" + s + "\n" + g;
        txtArea.setText(ultimoRecordInserito);
        caricaComboGite();
        preparaNuovoStudente();

    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Il costo deve essere un numero.");
    } catch (java.io.IOException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Errore nel salvataggio: " + ex.getMessage());
    }
}

    private void leggiTutti() {
        try {
            java.util.ArrayList<Gita> gite = gestione.leggiTutti();
            if (gite.isEmpty()) { txtArea.setText("Nessun record trovato."); return; }
            StringBuilder sb = new StringBuilder();
            for (Gita g : gite) {
                sb.append("=== ").append(g).append(" ===\n");
                for (Studente s : g.getStudentiPartecipanti())
                    sb.append("  ").append(s).append("\n");
                sb.append("\n");
            }
            txtArea.setText(sb.toString());
        } catch (java.io.IOException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Errore in lettura: " + ex.getMessage());
        }
    }

    private void cercaPerGita() {
    String nomeGita = jComboBox1.getSelectedItem() != null
            ? jComboBox1.getSelectedItem().toString().trim()
            : "";

    if (nomeGita.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selezionare una gita.");
        return;
    }

    try {
        java.util.ArrayList<Studente> studenti = gestione.cercaStudentiPerGita(nomeGita);

        if (studenti.isEmpty()) {
            txtArea.setText("Nessuno studente trovato per: " + nomeGita);
            return;
        }

        StringBuilder sb = new StringBuilder();
        sb.append("Studenti iscritti a \"").append(nomeGita).append("\":\n\n");

        for (Studente s : studenti) {
            sb.append("  ").append(s).append("\n");
        }

        txtArea.setText(sb.toString());

    } catch (java.io.IOException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Errore in lettura: " + ex.getMessage());
    }
}

    private void svuotaCampi() {
    txtNome.setText("");
    txtCognome.setText("");
    txtClasse.setText("");
    txtNomeGita.setText("");
    txtDestinazione.setText("");
    txtCosto.setText("");
    chkIscritto.setSelected(true);
    }
    
    private void caricaComboGite() {
    try {
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();

        java.util.ArrayList<Gita> gite = gestione.leggiTutti();

        for (Gita g : gite) {
            jComboBox1.addItem(g.getNome());
            jComboBox2.addItem(g.getNome());
        }

    } catch (java.io.IOException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Errore nel caricamento gite: " + ex.getMessage());
    }
} 
    
